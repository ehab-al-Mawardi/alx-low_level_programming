#include "3-calc.h"

/**
 * op_add - adds two numbers.
 *
 * @a: first num.
 * @b: second num.
 *
 * Return: integer.
*/

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracte two numbers.
 *
 * @a: first num.
 * @b:second num.
 *
 * Return: integer.
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplate two numbers.
 *
 * @a: first num.
 * @b:second num.
 *
 * Return: integer.
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers.
 *
 * @a: first num.
 * @b:second num.
 *
 * Return: integer.
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -  return remainder of division of (a) by (b).
 *
 * @a: first num.
 * @b:second num.
 *
 * Return: integer.
*/
int op_mod(int a, int b)
{
	return (a % b);
}
